using UnityEngine;

// This script is attached to each food item in the game.
// It stores the name and sprite of the food item.
public class FoodItem : MonoBehaviour
{
    // The name of the food item (e.g., "Tomato", "Cheese").
    public string itemName;

    // The sprite that represents the food item visually.
    public Sprite itemSprite;
}
